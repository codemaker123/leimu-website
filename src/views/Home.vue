import fullpage from 'fullpage.js';
import Footer from "@/components/Footer";
import videojs from 'video.js';
import 'video.js/dist/video-js.css';
import 'videojs-contrib-hls';

export default {
  components: {
    Footer
  },
  data() {
    return {
      showFooter: false, // 控制是否显示Footer
      fullPageInstance: null,
      screenWidth: 0,
      screenHeight: 0,
      breakpoint: 1920, // 设定断点宽度
    };
  },
  created() {

  },
  mounted() {
    // document.getElementById("fullpage")
    if(window.fullpage_api != null){
      window.fullpage_api.destroy('all');
    }
    this.fullPageInstance = new fullpage('#fullpage', {
      // 设置选项和配置
      navigation: true, // 显示导航小圆点
      responsiveWidth: 768, // 设置屏幕宽度阈值，低于该值时触发自适应
      responsiveHeight: 0, // 设置屏幕高度阈值，0表示始终触发自适应
      responsiveSlides: true, // 是否对幻灯片进行自适应
      onLeave: (origin, destination, direction) => {
        // 滚动到最后一屏时显示Footer
        if (destination.index === 3) {
          this.showFooter = true;
        } else {
          this.showFooter = false;
        }
      }
    });
    this.getScreenSize();
    window.addEventListener('resize', this.getScreenSize);
  },
  beforeUnmount() {
    // 在组件销毁前清除 fullPage.js 对象
    this.fullPageInstance = null
  },
  computed() {
    isLargeScreen()
    {
      return this.screenWidth >= this.breakpoint;
    }
  },
  methods: {
    getScreenSize() {
      this.screenWidth = window.innerWidth;
      this.screenHeight = window.innerHeight;
    }
  }
};
</script>

